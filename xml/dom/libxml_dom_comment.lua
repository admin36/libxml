function libxml.dom.createCommentNodeObj(pText)
    local self = libxml.dom.createCharacterDataNodeObj(pText)
    
    --===================================================================
    -- PROPERTIES                                                       =
    --===================================================================
    self.nodeName = "#comment"    
    ---------------------------------------------------------------------    
    
    --===================================================================
    -- MUTATORS                                                         =
    --===================================================================    
    
    --===================================================================
    -- METHODS                                                            =    
    --===================================================================    
    
    return self
end